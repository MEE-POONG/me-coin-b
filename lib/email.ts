// ‡∏™‡∏£‡πâ‡∏≤‡∏á transporter ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏™‡πà‡∏á email
const transporter = nodemailer.createTransport({
  service: 'gmail',
  auth: {
    user: process.env.GMAIL_USER || 'me.prompt.tec@gmail.com',
    pass: process.env.GMAIL_PASSWORD, // App Password ‡∏à‡∏≤‡∏Å Google
  },
})

interface SendEmailParams {
  to: string
  subject: string
  html: string
  text?: string
}

export async function sendEmail({ to, subject, html, text }: SendEmailParams) {
  try {
    const info = await transporter.sendMail({
      from: `"MeCoins" <${process.env.GMAIL_USER || 'me.prompt.tec@gmail.com'}>`,
      to,
      subject,
      html,
      text: text || html.replace(/<[^>]*>/g, ''), // Strip HTML ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö plain text
    })

    console.log('‚úÖ Email sent:', info.messageId)
    return { success: true, messageId: info.messageId }
  } catch (error) {
    console.error('‚ùå Error sending email:', error)
    return { success: false, error }
  }
}

